<h2>LOOPS</h2>
<?php

//for loop 
for($i=0;$i<=10;$i++){
    echo"for loop at $i value <br>";
}
echo "<br>";

//while loop
$a=0;
while($a<=5){
    echo "while loop at $a value <br>";
    $a++;
}
echo "<br>";

//do while loop
$b=0;
do{
    echo "its a do while here at $b <br>";
    $b++;
}while($b<=5);
?>